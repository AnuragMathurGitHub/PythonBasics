# Defining a function
def greet(name):
    print(f"Hello, {name}!")

# Calling a function
greet("Anurag")

# Function with Return Value
def add_numbers(a, b):
    return a + b

result = add_numbers(10, 5)
print("Sum is:", result)

# Key Function Concepts:
# def - Define a function
# return - Return a value from a function
# Parameters - Input values to the function
